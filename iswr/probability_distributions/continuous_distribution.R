# measurements on a continuous scale, temperature, concentrations
# records will be to a finite precision but disregarded in models
# there exists a random variation that follow patterns and cluster
# around a central value with large deviations being rare and 
# small being more frequent. with infinitly many zeros being close
# there cant be a probability of a particular value being zero 
# since there is no point probability the concept of 
# \textit{density} with the infinitesimal probability of a small
# region around x divided by the size of the region and defined by
# the relation $F(x) = \int_{-\infty}^{x} f(x) dx$

# the \textit{uniform distribution} has a constant density over a
# specified interval by default [0,1]

# the \textit{normal distribution} (\textit{Gaussian distribution})
# has a density $f(x) = \frac{1}{\sqrt{2\pi\sigma} exp\bigg(-
# \frac{(x-\mu)^{2}}{2\sigma^{2} \bigg)$
# it creates a bell shaped characteristic and is useful for
# approximating distributions in example the binomial distribution 
# for large sample sizes can be suitably scaled to a normal distribution
